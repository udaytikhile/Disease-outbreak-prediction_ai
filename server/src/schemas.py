from marshmallow import Schema, fields, validate, validates, ValidationError

class HeartPredictionSchema(Schema):
    age = fields.Float(required=True, validate=validate.Range(min=0, max=120))
    sex = fields.Float(required=True, validate=validate.OneOf([0, 1]))
    cp = fields.Float(required=True, validate=validate.Range(min=0, max=3))
    trestbps = fields.Float(required=True, validate=validate.Range(min=0, max=300))
    chol = fields.Float(required=True, validate=validate.Range(min=0, max=600))
    fbs = fields.Float(required=True, validate=validate.OneOf([0, 1]))
    restecg = fields.Float(required=True, validate=validate.Range(min=0, max=2))
    thalach = fields.Float(required=True, validate=validate.Range(min=0, max=250))
    exang = fields.Float(required=True, validate=validate.OneOf([0, 1]))
    oldpeak = fields.Float(required=True, validate=validate.Range(min=0, max=10))
    slope = fields.Float(required=True, validate=validate.Range(min=0, max=2))
    ca = fields.Float(required=True, validate=validate.Range(min=0, max=4))
    thal = fields.Float(required=True, validate=validate.Range(min=0, max=3))

class DiabetesPredictionSchema(Schema):
    pregnancies = fields.Float(required=True, validate=validate.Range(min=0, max=20))
    glucose = fields.Float(required=True, validate=validate.Range(min=0, max=300))
    bloodPressure = fields.Float(required=True, validate=validate.Range(min=0, max=200))
    skinThickness = fields.Float(required=True, validate=validate.Range(min=0, max=100))
    insulin = fields.Float(required=True, validate=validate.Range(min=0, max=900))
    bmi = fields.Float(required=True, validate=validate.Range(min=0, max=70))
    dpf = fields.Float(required=True, validate=validate.Range(min=0, max=3))
    age = fields.Float(required=True, validate=validate.Range(min=0, max=120))

class ParkinsonsPredictionSchema(Schema):
    # Using generic float validation for sound measurements as ranges can vary widely
    fo = fields.Float(required=True)
    fhi = fields.Float(required=True)
    flo = fields.Float(required=True)
    jitter = fields.Float(required=True)
    jitterAbs = fields.Float(required=True)
    rap = fields.Float(required=True)
    ppq = fields.Float(required=True)
    ddp = fields.Float(required=True)
    shimmer = fields.Float(required=True)
    shimmerDb = fields.Float(required=True)
    apq3 = fields.Float(required=True)
    apq5 = fields.Float(required=True)
    apq = fields.Float(required=True)
    dda = fields.Float(required=True)
    nhr = fields.Float(required=True)
    hnr = fields.Float(required=True)
    rpde = fields.Float(required=True)
    dfa = fields.Float(required=True)
    spread1 = fields.Float(required=True)
    spread2 = fields.Float(required=True)
    d2 = fields.Float(required=True)
    ppe = fields.Float(required=True)
